#Escribe un programa que solicite al usuario dos números enteros. Luego, muestra por pantalla la suma, resta, multiplicación y división de esos dos números.

primer_num = int(input("Ingresa el primer número"))
segundo_num = int(input("Ingresa el segundo número"))

suma = primer_num + segundo_num
resta = primer_num - segundo_num
multiplicacion = primer_num * segundo_num
division = primer_num / segundo_num

print("Suma: ", suma)
print("Resta: ", resta)
print("Multiplicación: ", multiplicacion)
print("División: ", division)
